from Idiomas.Idioma import Idioma

class Espanol(Idioma):
    def saludar(self):
        return "Hola"

    def despedirse(self):
        return "Adiós"

    def perdon(self):
        return "Perdón"

    def pedirCafe(self):
        return "¿Puedes darme un café?"

    def cuantoCuesta(self):
        return "¿Cuanto cuesta esto?"

    def dondeQueda(self):
        return "¿Donde queda?"
