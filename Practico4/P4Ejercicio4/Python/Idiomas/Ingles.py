from Idiomas.Idioma import Idioma

class Ingles(Idioma):
    def saludar(self):
        return "Hello"

    def despedirse(self):
        return "Goodbye"

    def perdon(self):
        return "Sorry"

    def pedirCafe(self):
        return "Can I have a coffee?"

    def cuantoCuesta(self):
        return "How much does it cost?"

    def dondeQueda(self):
        return "Where is it?"
